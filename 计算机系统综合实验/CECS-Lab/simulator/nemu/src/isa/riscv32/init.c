#include <isa.h>
#include <memory/paddr.h>

static void restart() {
  /* Set the initial program counter. */
  cpu.pc = RESET_VECTOR;

  /* The zero register is always 0. */
  cpu.gpr[0] = 0;
}

void init_isa() {
  /* Initialize this virtual computer system. */
  restart();
}
